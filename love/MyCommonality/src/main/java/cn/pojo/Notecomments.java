package cn.pojo;

public class Notecomments {
    private Integer ncid;

    private String ncreply;

    private Integer nclike;

    public Integer getNcid() {
        return ncid;
    }

    public void setNcid(Integer ncid) {
        this.ncid = ncid;
    }

    public String getNcreply() {
        return ncreply;
    }

    public void setNcreply(String ncreply) {
        this.ncreply = ncreply == null ? null : ncreply.trim();
    }

    public Integer getNclike() {
        return nclike;
    }

    public void setNclike(Integer nclike) {
        this.nclike = nclike;
    }
}